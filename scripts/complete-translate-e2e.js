// íŒŒì¼ ë‚´ìš© ë³€í™˜
function translateFile(filePath) {
  try {
    // UTF-8ë¡œ ì½ê¸°
    let content = fs.readFileSync(filePath, 'utf8');
    
    // í•œêµ­ì–´ê°€ í¬í•¨ë˜ì–´ ìˆëŠ”ì§€ í™•ì¸
    if (!/[ê°€-í£]+/.test(content)) {
      return false;
    }
    
    // ë¼ì¸ë³„ë¡œ ì²˜ë¦¬
    const lines = content.split('\n');
    const translatedLines = [];
    let changed = false;
    
    for (let line of lines) {
      const originalLine = line;
      
      // í•œêµ­ì–´ê°€ ì—†ìœ¼ë©´ ê·¸ëŒ€ë¡œ ìœ ì§€
      if (!/[ê°€-í£]+/.test(line)) {
        translatedLines.push(line);
        continue;
      }
      
      // ì£¼ì„ ë¼ì¸ì¸ ê²½ìš°
      if (line.trim().startsWith('//')) {
        // ì£¼ì„ ì „ì²´ë¥¼ ë³€í™˜ (ë” ë§ì€ ë°˜ë³µ, í•œêµ­ì–´ê°€ ì—†ì–´ì§ˆ ë•Œê¹Œì§€)
        let prevLine = '';
        for (let i = 0; i < 100; i++) {
          prevLine = line;
          line = translateKoreanToJapanese(line);
          if (line === prevLine && !/[ê°€-í£]+/.test(line)) break;
        }
      } else {
        // ì¼ë°˜ ì½”ë“œ ë¼ì¸: ë¬¸ìì—´ ë³´í˜¸
        const stringPlaceholders = [];
        let placeholderIndex = 0;
        
        // ë¬¸ìì—´ ì¹˜í™˜
        line = line.replace(/'([^']*)'/g, (match, strContent) => {
          const placeholder = `__STR_S_${placeholderIndex}__`;
          stringPlaceholders.push({ placeholder, content: strContent, quote: "'" });
          placeholderIndex++;
          return placeholder;
        });
        
        line = line.replace(/"([^"]*)"/g, (match, strContent) => {
          const placeholder = `__STR_D_${placeholderIndex}__`;
          stringPlaceholders.push({ placeholder, content: strContent, quote: '"' });
          placeholderIndex++;
          return placeholder;
        });
        
        line = line.replace(/`([^`]*)`/g, (match, strContent) => {
          const placeholder = `__STR_B_${placeholderIndex}__`;
          stringPlaceholders.push({ placeholder, content: strContent, quote: '`' });
          placeholderIndex++;
          return placeholder;
        });
        
        // ë‚˜ë¨¸ì§€ ë¶€ë¶„ ë³€í™˜ (ë” ë§ì€ ë°˜ë³µ, í•œêµ­ì–´ê°€ ì—†ì–´ì§ˆ ë•Œê¹Œì§€)
        let prevLine2 = '';
        for (let i = 0; i < 100; i++) {
          prevLine2 = line;
          line = translateKoreanToJapanese(line);
          if (line === prevLine2 && !/[ê°€-í£]+/.test(line)) break;
        }
        
        // ë¬¸ìì—´ ë³µì› ë° ë³€í™˜
        for (const { placeholder, content, quote } of stringPlaceholders) {
          if (/[ê°€-í£]+/.test(content)) {
            let translatedContent = content;
            let prevContent2 = '';
            for (let i = 0; i < 100; i++) {
              prevContent2 = translatedContent;
              translatedContent = translateKoreanToJapanese(translatedContent);
              if (translatedContent === prevContent2 && !/[ê°€-í£]+/.test(translatedContent)) break;
            }
            line = line.replace(placeholder, quote + translatedContent + quote);
          } else {
            line = line.replace(placeholder, quote + content + quote);
          }
        }
      }
      
      // ìµœì¢… í™•ì¸ (í•œêµ­ì–´ê°€ ì—†ì–´ì§ˆ ë•Œê¹Œì§€ ë°˜ë³µ)
      let finalPrevLine = '';
      for (let i = 0; i < 100; i++) {
        if (!/[ê°€-í£]+/.test(line)) break;
        finalPrevLine = line;
        line = translateKoreanToJapanese(line);
        if (line === finalPrevLine) break;
      }
      
      if (line !== originalLine) {
        changed = true;
      }
      translatedLines.push(line);
    }
    
    const result = translatedLines.join('\n');
    
    if (changed && result !== content) {
      // UTF-8ë¡œ ì €ì¥ (BOM ì—†ì´)
      fs.writeFileSync(filePath, result, 'utf8');
      return true;
    }
    
    return false;
  } catch (error) {
    console.error(`  âŒ ì˜¤ë¥˜: ${filePath}`, error.message);
    return false;
  }
}

// ë””ë ‰í† ë¦¬ ì²˜ë¦¬
function processDirectory(dirPath, excludePatterns = []) {
  const files = fs.readdirSync(dirPath);
  let translatedCount = 0;
  
  for (const file of files) {
    const filePath = path.join(dirPath, file);
    const stat = fs.statSync(filePath);
    
    const shouldExclude = excludePatterns.some(pattern => {
      if (typeof pattern === 'string') {
        return filePath.includes(pattern);
      }
      return pattern.test(filePath);
    });
    
    if (shouldExclude) continue;
    
    if (stat.isDirectory()) {
      translatedCount += processDirectory(filePath, excludePatterns);
    } else if (file.endsWith('.ts') || file.endsWith('.js')) {
      if (file.endsWith('.md') || file.endsWith('.txt')) continue;
      
      const translated = translateFile(filePath);
      if (translated) {
        translatedCount++;
        console.log(`  âœ… ë³€í™˜: ${filePath}`);
      }
    }
  }
  
  return translatedCount;
}

